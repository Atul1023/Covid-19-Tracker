}

class _FaqState extends State<Faq> {
  List<Faqs> faqs;

  @override
  void initState() {
    super.initState();
    faqs = faqList;
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        elevation: 0,
        backgroundColor: Colors.white,
        leading: GestureDetector(
            onTap: () => Navigator.of(context).pop(),
